# a
def sqrt2(x, e = 0.0000001)
  # y = x ** 2
  a = 0
  b = x
  while (a - b).abs > e
    c = (a + b) / 2.0
    if c ** 2 > x
      b = c
    else
      a = c
    end
  end
  c
end

sqrt2(10)  # => 3.16227771341801
sqrt2(20)  # => 4.47213597595692
sqrt2(30)  # => 5.47722553834319
sqrt2(49)  # => 7.00000003911555
sqrt2(100) # => 10.0000000558794

