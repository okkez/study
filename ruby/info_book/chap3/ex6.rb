def sqrt3(n, e = 0.00000001)
  r = 0
  r1 = n
  while (r1 - r).abs > e
    r = r1
    r1 = r / 2.0 + n / (2 * r.to_f)
  end
  r
end

sqrt3(10)  # => 3.16227766517567
sqrt3(20)  # => 4.47213595500161
sqrt3(30)  # => 5.47722557564769
sqrt3(49)  # => 7.0
sqrt3(100) # => 10.0000000001399
